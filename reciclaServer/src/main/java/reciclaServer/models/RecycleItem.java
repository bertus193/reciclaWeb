package reciclaServer.models;

import com.fasterxml.jackson.annotation.JsonFormat;
import com.fasterxml.jackson.annotation.JsonIdentityInfo;
import com.fasterxml.jackson.annotation.JsonIdentityReference;
import com.fasterxml.jackson.annotation.ObjectIdGenerators;
import reciclaServer.config.EntityIdResolver;

import javax.persistence.*;
import javax.validation.constraints.NotNull;
import java.sql.Timestamp;

@Entity
@Table(name = "recycle_items")
@JsonIdentityInfo(
        generator = ObjectIdGenerators.PropertyGenerator.class,
        resolver = EntityIdResolver.class,
        scope = RecycleItem.class,
        property = "id")
public class RecycleItem {

    // An autogenerated id (unique for each user in the db)
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private long id;

    private String name;

    private String image;

    @ManyToOne
    @JoinColumn(name = "recycle_user")
    @JsonIdentityReference(alwaysAsId = true)
    private User recycleUser;

    @ManyToOne
    @JoinColumn(name = "storage")
    @JsonIdentityReference(alwaysAsId = true)
    private Storage storage;

    @ManyToOne
    @NotNull
    @JoinColumn(name = "item_type")
    private ItemType itemType;

    @JsonFormat(pattern="yyyy-MM-dd'T'HH:mm:ss.SSSXXX") //Formato necesario para panel admin (new Date())
    private Timestamp createdDate;

    public RecycleItem() { //Needed for JPA

    }

    public String getName() {
        return name;
    }

    public long getId() {
        return id;
    }

    public String getImage() {
        return image;
    }

    public User getRecycleUser() {
        return recycleUser;
    }

    public ItemType getItemType() {
        return itemType;
    }

    public Storage getStorage() {
        return storage;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public void setRecycleUser(User recycleUser) {
        this.recycleUser = recycleUser;
    }

    public void setStorage(Storage storage) {
        this.storage = storage;
    }

    public void setItemType(ItemType itemType) {
        this.itemType = itemType;
    }

    public Timestamp getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Timestamp createdDate) {
        this.createdDate = createdDate;
    }
}
